const RCON: ReadonlyArray<number> = [0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1b,0x36];

export const AES_BLOCK_SIZE = 16;
const AES_KEY_SIZE = 32;
const AES_ROUNDS = 14;
const NK = 8;

function xtime(a: number): number {
  return ((a << 1) ^ ((a & 0x80) ? 0x1b : 0x00)) & 0xff;
}

function gmul(a: number, b: number): number {
  let r = 0, t = a;
  for (let i = 0; i < 8; i++) {
    if (b & 1) r ^= t;
    t = xtime(t);
    b >>= 1;
  }
  return r & 0xff;
}

function keyExpansion(key: Uint8Array): Uint32Array {
  if (key.length !== AES_KEY_SIZE) throw new Error(`AES-256 requires 32-byte key`);
  const total = 4 * (AES_ROUNDS + 1);
  const W = new Uint32Array(total);
  for (let i = 0; i < NK; i++) {
    W[i] = ((key[4*i]<<24)|(key[4*i+1]<<16)|(key[4*i+2]<<8)|key[4*i+3])>>>0;
  }
  for (let i = NK; i < total; i++) {
    let t = W[i-1];
    if (i % NK === 0) {
      t = ((t<<8)|(t>>>24))>>>0;
      t = ((S_BOX[(t>>>24)&0xff]<<24)|(S_BOX[(t>>>16)&0xff]<<16)|(S_BOX[(t>>>8)&0xff]<<8)|S_BOX[t&0xff])>>>0;
      t = (t ^ (RCON[i/NK-1]<<24))>>>0;
    } else if (i % NK === 4) {
      t = ((S_BOX[(t>>>24)&0xff]<<24)|(S_BOX[(t>>>16)&0xff]<<16)|(S_BOX[(t>>>8)&0xff]<<8)|S_BOX[t&0xff])>>>0;
    }
    W[i] = (W[i-NK] ^ t) >>> 0;
  }
  return W;
}

function subBytes(s: Uint8Array) { for (let i=0;i<16;i++) s[i]=S_BOX[s[i]]; }
function invSubBytes(s: Uint8Array) { for (let i=0;i<16;i++) s[i]=INV_S_BOX[s[i]]; }

function shiftRows(s: Uint8Array) {
  let t=s[1]; s[1]=s[5]; s[5]=s[9]; s[9]=s[13]; s[13]=t;
  t=s[2]; const t2=s[6]; s[2]=s[10]; s[6]=s[14]; s[10]=t; s[14]=t2;
  t=s[15]; s[15]=s[11]; s[11]=s[7]; s[7]=s[3]; s[3]=t;
}

function invShiftRows(s: Uint8Array) {
  let t=s[13]; s[13]=s[9]; s[9]=s[5]; s[5]=s[1]; s[1]=t;
  t=s[2]; const t2=s[6]; s[2]=s[10]; s[6]=s[14]; s[10]=t; s[14]=t2;
  t=s[3]; s[3]=s[7]; s[7]=s[11]; s[11]=s[15]; s[15]=t;
}

function mixColumns(s: Uint8Array) {
  for (let c=0;c<4;c++) {
    const i=c*4, a=s[i],b=s[i+1],d=s[i+2],e=s[i+3];
    s[i]=gmul(2,a)^gmul(3,b)^d^e;
    s[i+1]=a^gmul(2,b)^gmul(3,d)^e;
    s[i+2]=a^b^gmul(2,d)^gmul(3,e);
    s[i+3]=gmul(3,a)^b^d^gmul(2,e);
  }
}

function invMixColumns(s: Uint8Array) {
  for (let c=0;c<4;c++) {
    const i=c*4, a=s[i],b=s[i+1],d=s[i+2],e=s[i+3];
    s[i]=gmul(14,a)^gmul(11,b)^gmul(13,d)^gmul(9,e);
    s[i+1]=gmul(9,a)^gmul(14,b)^gmul(11,d)^gmul(13,e);
    s[i+2]=gmul(13,a)^gmul(9,b)^gmul(14,d)^gmul(11,e);
    s[i+3]=gmul(11,a)^gmul(13,b)^gmul(9,d)^gmul(14,e);
  }
}

function addRoundKey(s: Uint8Array, W: Uint32Array, r: number) {
  for (let c=0;c<4;c++) {
    const w=W[r*4+c];
    s[c*4]^=(w>>>24)&0xff; s[c*4+1]^=(w>>>16)&0xff;
    s[c*4+2]^=(w>>>8)&0xff; s[c*4+3]^=w&0xff;
  }
}

/** Cifra un bloque de 16 bytes con AES-256 */
export function aesEncryptBlock(block: Uint8Array, key: Uint8Array): Uint8Array {
  if (block.length!==16) throw new Error('Block must be 16 bytes');
  const W = keyExpansion(key);
  const s = new Uint8Array(block);
  addRoundKey(s, W, 0);
  for (let r=1; r<AES_ROUNDS; r++) { subBytes(s); shiftRows(s); mixColumns(s); addRoundKey(s,W,r); }
  subBytes(s); shiftRows(s); addRoundKey(s, W, AES_ROUNDS);
  return s;
}

/** Descifra un bloque de 16 bytes con AES-256 */
export function aesDecryptBlock(block: Uint8Array, key: Uint8Array): Uint8Array {
  if (block.length!==16) throw new Error('Block must be 16 bytes');
  const W = keyExpansion(key);
  const s = new Uint8Array(block);
  addRoundKey(s, W, AES_ROUNDS);
  for (let r=AES_ROUNDS-1; r>=1; r--) { invShiftRows(s); invSubBytes(s); addRoundKey(s,W,r); invMixColumns(s); }
  invShiftRows(s); invSubBytes(s); addRoundKey(s, W, 0);
  return s;
}
